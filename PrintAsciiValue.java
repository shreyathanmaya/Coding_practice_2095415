public class PrintAsciiValue   
{  
	public static void main(String[] args)   
	{  
		char ch1 = 'p';
		char ch2 = 'Q';
		int asciivalue1 = ch1;
		int asciivalue2 = ch2;
		System.out.println("The ASCII value of " + ch1 + "is: " +asciivalue1);
		System.out.println("The ASCII value of " + ch2 + "is: " +asciivalue2);
	}  
}  